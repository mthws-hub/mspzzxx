    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        DefaultTableModel model = (DefaultTableModel) tabPainting.getModel();

        if (tabPainting.isEditing()) {
            tabPainting.getCellEditor().stopCellEditing();
        }

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No data to update.");
            return;
        }

        int successCount = 0;
        int errorCount = 0;

        // Recorremos la tabla visual fila por fila
        for (int i = 0; i < model.getRowCount(); i++) {
            try {
                // Obtenemos el ID tal cual está en la tabla
                String id = String.valueOf(model.getValueAt(i, 0));
                String name = String.valueOf(model.getValueAt(i, 1));
                String colorsText = String.valueOf(model.getValueAt(i, 2));

                // Convertimos el precio. Si falla, avisamos.
                double price = Double.parseDouble(model.getValueAt(i, 3).toString());

                // Convertimos colores
                List<String> colorsList = parseColors(colorsText);

                // Llamamos al controller (que ahora sí valida si existe el ID)
                boolean actualizado = controller.updatePainting(id, name, price, colorsList);

                if (actualizado) {
                    successCount++;
                } else {
                    errorCount++;
                    System.out.println("No se pudo actualizar ID: " + id + " (Probablemente no existe en MongoDB)");
                }

            } catch (Exception e) {
                System.out.println("Error en fila " + i + ": " + e.getMessage());
            }
        }
        
        if (successCount > 0) {
            JOptionPane.showMessageDialog(this, successCount + " paintings were updated");
            loadTableData(null); // Recargamos para ver el IVA calculado desde la BD
        } else if (errorCount > 0) {
            JOptionPane.showMessageDialog(this, "Error, ID didn't find");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        String idToSearch = txtId.getText();

        if (idToSearch.isEmpty()) {
            // Si está vacío, recargamos toda la tabla
            loadTableData(null);
        } else {
            // 2. Buscamos usando el Id
            Painting foundPainting = controller.findPaintingById(idToSearch);

            if (foundPainting != null) {
                // 3. Mostramos solo ese resultado en la lista
                List<Painting> results = new ArrayList<>();
                results.add(foundPainting);
                loadTableData(results);
            } else {
                JOptionPane.showMessageDialog(this, "Painting with ID " + idToSearch + " not found.");
                loadTableData(null); // Limpiamos si no encuentra
            }
        }
    }//GEN-LAST:event_btnFindActionPerformed

