    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        try {
            String id = txtId.getText();
            String name = txtName.getText();
            double price = Double.parseDouble(txtBasePrice.getText());
            List<String> colors = parseColors(txtColors.getText());

            if (controller.createPainting(id, name, price, colors)) {
                JOptionPane.showMessageDialog(this, "Painting Save");
                clearForm();
                loadTableData(null);
            } else {
                JOptionPane.showMessageDialog(this, "Error to save");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Prices's format is wrong");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tabPainting.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una fila para eliminar.");
            return;
        }

        // Detener edición por si acaso
        if (tabPainting.isEditing()) {
            tabPainting.getCellEditor().stopCellEditing();
        }

        // Obtener ID de la columna 0
        String idToDelete = tabPainting.getValueAt(selectedRow, 0).toString();

        int confirm = JOptionPane.showConfirmDialog(this, "¿Are you sure to want to delete the ID: " + idToDelete + "?");
        if (confirm == JOptionPane.YES_OPTION) {

            boolean deleted = controller.deletePainting(idToDelete);

            if (deleted) {
                JOptionPane.showMessageDialog(this, "Eliminado correctamente de MongoDB.");
                loadTableData(null);
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Error: No se pudo eliminar. El ID no se encontró en la base de datos.");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
    private void loadTableData(List<Painting> dataList) {
        DefaultTableModel model = (DefaultTableModel) tabPainting.getModel();
        model.setRowCount(0); // Limpiar tabla

        // Si dataList es null, traemos todo del controlador
        List<Painting> list = (dataList == null) ? controller.getAllPaintings() : dataList;

        for (Painting painting : list) {
            Object[] row = {
                String.valueOf(painting.getId()),
                painting.getName(),
                String.join(", ", painting.getColors()), // Convertimos la lista de colores a texto para que se vea y NO SE PIERDA
                painting.getPrice(),
                painting.getPriceWithIva()
            };
            model.addRow(row);
        }
    }

    // Convierte texto "Rojo, Azul" -> List["Rojo", "Azul"]
    private List<String> parseColors(String text) {
        if (text == null || text.trim().isEmpty()) {
            return new ArrayList<>();
        }

        // Split por coma, y trim a cada elemento para quitar espacios
        return Arrays.stream(text.split(","))
                .map(String::trim)
                .collect(Collectors.toList());
    }

